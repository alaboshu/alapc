<template>
  <x-border :title="title">
    <!-- <x-table :config="viewModel" v-if="viewModel"></x-table> -->
    <zk-table-video-list v-if="async"></zk-table-video-list>
  </x-border>
</template>
<script>
  export default {
    props: {
      widgetData: {}
    },
    data () {
      return {
        async: false,
        viewModel: null,
        title: '数据编辑'
      }
    },

    mounted () {
      this.init()
    },
    methods: {
      async  init () {
        this.async = false
        var type = this.$crud.getType()
        var para = {
          type: type
        }
        var response = await this.$api.httpGet('/Api/Auto/news', para)
        if (response.status === 1) {
          this.viewModel = response.result
          this.title = this.viewModel.name
          this.async = true
        } else {
          this.$alert(response.message, '类型输入错误', {
            confirmButtonText: '确定'
          })
        }
      },
      getPath () {
        this.init()
      }
    },
    watch: {
      '$route': 'getPath'
    }
  }
</script>
